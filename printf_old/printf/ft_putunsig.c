#include "ft_printf.h"

int	ft_putunsig(size_t n)
{
	if (n / 10 != 0)
	{
		ft_putunsig(n / 10);
	}
	ft_putchar((char){'0' + (n % 10)});
	return (count_digits(n, 10));
}
